def get_sources_for_topic(topic: str) -> list[str]:
    """منابع مربوط به موضوع را برمی‌گرداند"""
    # جستجوی دقیق
    if topic in VERIFIED_SOURCES:
        return VERIFIED_SOURCES[topic]
    # جستجوی جزئی
    for key, sources in VERIFIED_SOURCES.items():
        if any(word in topic for word in key.split() if len(word) > 3):
            return sources
    return []


def format_sources(sources: list[str]) -> str:
    """منابع را برای درج در پرامپت فرمت می‌کند"""
    if not sources:
        return ""
    formatted = "\n".join(f"- {s}" for s in sources)
    return f"\n\nSOURCE_PACK (ONLY use these verified real sources — do not add others):\n{formatted}"
